package ejercicio02v2;

public class Empresa {

	private String nombreEmpresa;
	private int cif;
	public Empresa(String nombreEmpresa, int cif) {
		super();
		this.nombreEmpresa = nombreEmpresa;
		this.cif = cif;
	}
	@Override
	public String toString() {
		return "Empresa [nombreEmpresa=" + nombreEmpresa + ", cif=" + cif + "]";
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}
